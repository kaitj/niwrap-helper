def main() -> None:
    """Generate bids.py and test_bids.py from the BIDS schema."""
    schema = load_schema()
    bids_version: str = schema.bids_version

    # --- Collect schema data ------------------------------------------------

    label_pattern: str = schema.objects.formats.label.pattern
    entity_order_long: list[str] = list(schema.rules.entities)

    entities: list[dict[str, object]] = []
    for long_name in entity_order_long:
        ent = schema.objects.entities[long_name]
        desc_raw = str(ent.description) if hasattr(ent, "description") else ""
        entities.append(
            {
                "key": ent.name,
                "long_name": long_name,
                "display_name": ent.display_name,
                "format": ent.format,
                "enum": tuple(ent.enum) if hasattr(ent, "enum") else None,
                "description": clean_desc(desc_raw),
            }
        )

    datatypes = sorted(
        (dt.value, clean_desc(dt.display_name))
        for dt in schema.objects.datatypes.values()
    )
    suffixes = sorted(
        (suf.value, clean_desc(suf.display_name))
        for suf in schema.objects.suffixes.values()
    )
    modalities = sorted(
        (key, clean_desc(mod.display_name))
        for key, mod in schema.objects.modalities.items()
    )

    all_extensions = sorted(ext.value for ext in schema.objects.extensions.values())
    compound_extensions = sorted(
        (v for v in all_extensions if v.startswith(".") and v[1:].count(".") >= 1),
        key=len,
        reverse=True,
    )

    # --- Generate & write module --------------------------------------------

    module_src = generate_module(
        bids_version=bids_version,
        entities=entities,
        datatypes=datatypes,
        suffixes=suffixes,
        modalities=modalities,
        compound_extensions=compound_extensions,
        label_pattern=label_pattern,
    )
    OUTPUT_MODULE.write_text(module_src, encoding="utf-8")
    print(f"Generated {OUTPUT_MODULE}")  # noqa: T201

    # --- Generate & write tests ---------------------------------------------

    tests_src = generate_tests(
        entities=entities,
        datatypes=datatypes,
        suffixes=suffixes,
        modalities=modalities,
    )
    OUTPUT_TESTS.write_text(tests_src, encoding="utf-8")
    print(f"Generated {OUTPUT_TESTS}")  # noqa: T201

    # --- Format & lint both files -------------------------------------------

    for path in (OUTPUT_MODULE, OUTPUT_TESTS):
        for cmd, label in [
            (["uv", "run", "ruff", "format", str(path)], "ruff format"),
            (
                ["uv", "run", "ruff", "check", "--fix", str(path)],
                "ruff check --fix",
            ),
        ]:
            try:
                subprocess.run(cmd, check=True, capture_output=True)  # noqa: S603
                print(f"  {path.name} {label}: ok")  # noqa: T201
            except (subprocess.CalledProcessError, FileNotFoundError) as exc:
                print(f"  {path.name} {label}: FAILED ({exc})")  # noqa: T201


if __name__ == "__main__":
    main()
