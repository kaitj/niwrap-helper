    w('            "sub-01", "anat", "sub-01_T1w.nii.gz"')
    w("        )")
    w("        assert result == expected")
    w()

    w("    def test_returns_purepath(self) -> None:")
    w('        """Return type is PurePath."""')
    w("        result = bids_path(")
    w('            sub="01",')
    w('            suffix="T1w",')
    w('            extension=".nii.gz",')
    w("            datatype=Datatype.ANAT,")
    w("        )")
    w("        assert isinstance(result, PurePath)")
    w()
    w()

    # ---- TestParseBidsName ----
    w("class TestParseBidsName:")
    w('    """Tests for parse_bids_name."""')
