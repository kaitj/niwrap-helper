"""Generate BIDS schema constants, utilities, and tests for the niwrap_helper package.

Reads the BIDS schema via bidsschematools and generates:
  - src/niwrap_helper/bids.py   (constants + build/parse functions)
  - tests/unit/test_bids.py (unit tests)

Run with:
    uv run scripts/generate_bids_tools.py
"""

