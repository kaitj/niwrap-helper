def generate_module(  # noqa: C901
    bids_version: str,
    entities: list[dict[str, object]],
    datatypes: list[tuple[str, str]],
    suffixes: list[tuple[str, str]],
    modalities: list[tuple[str, str]],
    compound_extensions: list[str],
    label_pattern: str,
) -> str:
    """Return the full source of src/niwrap_helper/bids.py."""
    lines: list[str] = []

    def w(s: str = "") -> None:
        lines.append(s)

    # -- File header --
    w(GENERATED_HEADER.rstrip())
    w('"""BIDS schema constants and utilities.')
    w()
    w(f"Auto-generated from BIDS schema v{bids_version}.")
    w("Regenerate with: ``uv run scripts/generate_bids_tools.py``")
    w('"""')
    w()
    w("from __future__ import annotations")
    w()
    w("import re")
    w("from dataclasses import dataclass")
    w("from pathlib import PurePath")
    w("from typing import Literal")
    w()
    w(f'BIDS_VERSION = "{bids_version}"')
    w()

    # -- Internal helpers --
    w(f'_LABEL_RE = re.compile(r"^{label_pattern}$")')
    w()
    ext_items = ", ".join(q(e) for e in compound_extensions)
    w(f"_COMPOUND_EXTENSIONS: tuple[str, ...] = ({ext_items},)")
    w()
    w()

    # -- Datatype namespace --
    w("class Datatype:")
    w('    """BIDS datatypes."""')
    w()
    for value, display in datatypes:
        w(f'    {to_const(value)}: Literal["{value}"] = "{value}"')
        w(f'    """{display}."""')
        w()
    w()

    # -- Suffix namespace --
    w("class Suffix:")
    w('    """BIDS suffixes."""')
    w()
    for value, display in suffixes:
        escaped = display.replace('"', '\\"')
        w(f'    {to_const(value)}: Literal["{value}"] = "{value}"')
        w(f'    """{escaped}."""')
        w()
    w()

    # -- Modality namespace --
    w("class Modality:")
    w('    """BIDS modalities."""')
    w()
    for value, display in modalities:
        w(f'    {to_const(value)}: Literal["{value}"] = "{value}"')
        w(f'    """{display}."""')
        w()
    w()

    # -- BIDSFile --
    w("@dataclass(frozen=True)")
    w("class BIDSFile:")
    w('    """Parsed components of a BIDS filename."""')
    w()
    w("    entities: dict[str, str]")
    w('    """Entity key-value pairs (e.g. ``{"sub": "01", "ses": "pre"}``)."""')
    w()
    w("    suffix: str")
    w('    """File suffix (e.g. ``"T1w"``, ``"bold"``)."""')
    w()
    w("    extension: str")
    w('    """File extension including leading dot (e.g. ``".nii.gz"``)."""')
    w()
    w()

    # -- Internal validation helper --
    w("def _format_entity(key: str, val: str | int) -> str:")
    w('    """Validate and format a single entity key-value pair."""')
    w("    if isinstance(val, int):")
    w("        if val < 0:")
    w("            raise ValueError(")
    w("                f\"Negative index for entity '{key}': {val}\"")
    w("            )")
    w('        return f"{key}-{val}"')
    w("    if not _LABEL_RE.fullmatch(val):")
    w("        raise ValueError(")
    w("            f\"Invalid label for entity '{key}': {val!r}. \"")
    w(f'            "Must match: {label_pattern}"')
    w("        )")
    w('    return f"{key}-{val}"')
    w()
    w()
    # -- Standard entity keys set (for extra-entity validation) --
    entity_keys = [str(e["key"]) for e in entities]
    w("_STANDARD_ENTITIES: frozenset[str] = frozenset({")
    for key in entity_keys:
        w(f'    "{key}",')
    w("})")
    w()
    w()

    # -- Typed parameter list: (key, type_hint, short_description, long_description) --
    params: list[tuple[str, str, str, str]] = []
    for e in entities:
        key = str(e["key"])
        fmt = str(e["format"])
        enum = e["enum"]
        display = str(e["display_name"])
        desc_raw = str(e.get("description") or "")
        sentence = first_sentence(desc_raw) if desc_raw else ""
        long_desc = f"{display}. {sentence}" if sentence else f"{display}."

        if enum is not None:
            lit = "Literal[" + ", ".join(q(str(v)) for v in enum) + "]"  # type: ignore[union-attr,attr-defined]
            hint = f"{lit} | None"
        elif fmt == "index":
            hint = "int | None"
        else:
            hint = "str | None"

        params.append((key, hint, display, long_desc))

    # Split params: everything before desc, then desc separately.
    # Extra (non-standard) entities are inserted just before desc.
    params_before_desc = [p for p in params if p[0] != "desc"]

    # -- bids_name --
    w("def bids_name(")
    w("    *,")
    for key, hint, _, _ in params:
        w(f"    {key}: {hint} = None,")
    w("    extra: dict[str, str | int] | None = None,")
    w("    suffix: str,")
    w("    extension: str,")
    w(") -> str:")
    w('    """Build a BIDS-compliant filename.')
    w()
    w("    Entities are ordered per the BIDS specification. Label values")
    w("    are validated against the BIDS label pattern. Index values")
    w("    must be non-negative integers and are converted to strings.")
    w()
    w("    Args:")
    for key, _, _, long_desc in params:
        for line in wrap_arg_doc(key, long_desc):
            w(line)
    w("        extra: Non-standard entities inserted just before")
    w("            ``desc`` (e.g.")
    w('            ``{"from": "T1w", "to": "template"}``).')
    w("            Values follow the same validation rules as")
    w("            standard entities.")
    w('        suffix: File suffix (e.g. "T1w", "bold").')
    w("        extension: File extension with leading dot")
    w('            (e.g. ".nii.gz").')
    w()
    w("    Returns:")
    w("        A BIDS-compliant filename string.")
    w()
    w("    Raises:")
    w("        ValueError: If a label value is invalid or an")
    w("            index is negative.")
    w('    """')

    # body — standard entities (except desc)
    w("    _entities: list[tuple[str, str | int | None]] = [")
    for key, _, _, _ in params_before_desc:
        w(f'        ("{key}", {key}),')
    w("    ]")
    w("    parts: list[str] = []")
    w("    for _key, _val in _entities:")
    w("        if _val is not None:")
    w("            parts.append(_format_entity(_key, _val))")
    # extra entities (with standard-key guard)
    w("    if extra is not None:")
    w("        _overlap = set(extra) & _STANDARD_ENTITIES")
    w("        if _overlap:")
    w("            raise ValueError(")
    w('                f"Standard entities passed via extra: {_overlap}. "')
    w('                "Use the corresponding keyword argument instead."')
    w("            )")
    w("        for _key, _val in extra.items():")
    w("            parts.append(_format_entity(_key, _val))")
    # desc (always last)
    w("    if desc is not None:")
    w('        parts.append(_format_entity("desc", desc))')
    w('    return "_".join([*parts, suffix]) + extension')
    w()
    w()

    # -- bids_path --
    w("def bids_path(")
    w("    *,")
    for key, hint, _, _ in params:
        w(f"    {key}: {hint} = None,")
    w("    extra: dict[str, str | int] | None = None,")
    w("    suffix: str,")
    w("    extension: str,")
    w("    datatype: str,")
    w(") -> PurePath:")
    w('    """Build a BIDS-compliant relative path.')
    w()
    w("    Constructs a path following the BIDS directory convention:")
    w("    ``sub-<label>/[ses-<label>/]<datatype>/<filename>``")
    w()
    w("    Args:")
    for key, _, _, long_desc in params:
        for line in wrap_arg_doc(key, long_desc):
            w(line)
    w("        extra: Non-standard entities inserted just before")
    w("            ``desc``. See :func:`bids_name` for details.")
    w('        suffix: File suffix (e.g. "T1w", "bold").')
    w("        extension: File extension with leading dot")
    w('            (e.g. ".nii.gz").')
    w("        datatype: BIDS datatype directory")
    w('            (e.g. "anat", "func").')
    w()
    w("    Returns:")
    w("        A BIDS-compliant relative path.")
    w('    """')

    w("    filename = bids_name(")
    for key, _, _, _ in params:
        w(f"        {key}={key},")
    w("        extra=extra,")
    w("        suffix=suffix,")
    w("        extension=extension,")
    w("    )")
    w("    dirs: list[str] = []")
    w("    if sub is not None:")
    w('        dirs.append(f"sub-{sub}")')
    w("    if ses is not None:")
    w('        dirs.append(f"ses-{ses}")')
    w("    dirs.append(datatype)")
    w("    return PurePath(*dirs, filename)")
    w()
    w()

    # -- parse_bids_name --
    w("def parse_bids_name(filename: str) -> BIDSFile:")
    w('    """Parse a BIDS filename into its components.')
    w()
    w("    Handles compound extensions (e.g. .nii.gz) and extracts")
    w("    all entity key-value pairs, suffix, and extension.")
    w()
    w("    Args:")
    w("        filename: A BIDS-compliant filename")
    w("            (without directory path).")
    w()
    w("    Returns:")
    w("        Parsed BIDS file components.")
    w('    """')
    w("    for ext in _COMPOUND_EXTENSIONS:")
    w("        if filename.endswith(ext):")
    w("            extension = ext")
    w("            stem = filename[: -len(ext)]")
    w("            break")
    w("    else:")
    w('        dot_idx = filename.rfind(".")')
    w("        if dot_idx == -1:")
    w('            extension = ""')
    w("            stem = filename")
    w("        else:")
    w("            extension = filename[dot_idx:]")
    w("            stem = filename[:dot_idx]")
    w('    parts = stem.split("_")')
    w("    suffix = parts[-1]")
    w("    entities: dict[str, str] = {}")
    w("    for part in parts[:-1]:")
    w('        key, _, value = part.partition("-")')
    w("        if value:")
    w("            entities[key] = value")
    w("    return BIDSFile(entities=entities, suffix=suffix, extension=extension)")
    w()

    return "\n".join(lines)


